const { execSync } = require('child_process');
const fs = require('fs');
const path = require('path');

const TAILWIND_ENTRY = `@import "tailwindcss";
@source "./app/**/*.{js,ts,jsx,tsx,mdx}";
@source "./components/**/*.{js,ts,jsx,tsx,mdx}";
@source "./pages/**/*.{js,ts,jsx,tsx,mdx}";
@source "./src/**/*.{js,ts,jsx,tsx,mdx}";

@custom-variant dark (&:is(.dark *));

@theme inline {
  --color-background: var(--background);
  --color-foreground: var(--foreground);
  --font-sans: var(--font-geist-sans);
  --font-mono: var(--font-geist-mono);
  --color-sidebar-ring: var(--sidebar-ring);
  --color-sidebar-border: var(--sidebar-border);
  --color-sidebar-accent-foreground: var(--sidebar-accent-foreground);
  --color-sidebar-accent: var(--sidebar-accent);
  --color-sidebar-primary-foreground: var(--sidebar-primary-foreground);
  --color-sidebar-primary: var(--sidebar-primary);
  --color-sidebar-foreground: var(--sidebar-foreground);
  --color-sidebar: var(--sidebar);
  --color-chart-5: var(--chart-5);
  --color-chart-4: var(--chart-4);
  --color-chart-3: var(--chart-3);
  --color-chart-2: var(--chart-2);
  --color-chart-1: var(--chart-1);
  --color-ring: var(--ring);
  --color-input: var(--input);
  --color-border: var(--border);
  --color-destructive: var(--destructive);
  --color-accent-foreground: var(--accent-foreground);
  --color-accent: var(--accent);
  --color-muted-foreground: var(--muted-foreground);
  --color-muted: var(--muted);
  --color-secondary-foreground: var(--secondary-foreground);
  --color-secondary: var(--secondary);
  --color-primary-foreground: var(--primary-foreground);
  --color-primary: var(--primary);
  --color-popover-foreground: var(--popover-foreground);
  --color-popover: var(--popover);
  --color-card-foreground: var(--card-foreground);
  --color-card: var(--card);
  --radius-sm: calc(var(--radius) - 4px);
  --radius-md: calc(var(--radius) - 2px);
  --radius-lg: var(--radius);
  --radius-xl: calc(var(--radius) + 4px);
}
`;

const root = path.resolve(__dirname, '..');
const inputPath = path.join(root, '.tmp-tailwind-in.css');
const outputPath = path.join(root, '.tmp-tailwind-out.css');
const outTsPath = path.join(root, 'app', 'tailwind-generated.ts');

try {
  fs.mkdirSync(path.dirname(inputPath), { recursive: true });
  fs.writeFileSync(inputPath, TAILWIND_ENTRY, 'utf8');

  execSync(`npx @tailwindcss/cli -i "${inputPath}" -o "${outputPath}"`, {
    cwd: root,
    stdio: 'inherit',
  });

  const css = fs.readFileSync(outputPath, 'utf8');
  const escaped = css
    .replace(/\\/g, '\\\\')
    .replace(/`/g, '\\`')
    .replace(/\$/g, '\\$');
  const tsContent = `// Generated by scripts/build-tailwind.js - do not edit
export const tailwindCss = \`${escaped}\`;
`;

  fs.writeFileSync(outTsPath, tsContent, 'utf8');
  console.log('Wrote app/tailwind-generated.ts');
} finally {
  try { fs.unlinkSync(inputPath); } catch (_) {}
  try { fs.unlinkSync(outputPath); } catch (_) {}
}
